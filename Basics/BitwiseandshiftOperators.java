package Basics;

public class BitwiseandshiftOperators {

	public static void main(String[] args) {
		
		//BitwiseandshiftOperators
		
		int a=5,b=8;
		/*
		 Binary Values
		0101
		1000
		 */
		System.out.println("Biwise And :" + (a&b));  //both same one - 1
		System.out.println("Biwise or  :" + (a|b));  //any one - 1
		System.out.println("Biwise Xor  :" + (a^b)); // both one is 0 and any one is 1
		System.out.println("Biwise Not  :" + (~a));  //~ tilled symbol - values print

	}
	}


