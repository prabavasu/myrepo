public class Conditional_Operators {
	
 	public static void main(String[] args) {
 		//Conditional Operators or Ternary Operators in java ?:
  int a=45,b=56;
  int c=a>b?a:b; //condition true assign  a in c otherwise condition false assign b in c
  System.out.println("The Greatest Num is " +c);
	}

}
